    return ZERO
end

-- 获取模型的半径
function ConstModelConfig.GetModelRadius(name)
    local t = model_config_table.data[name]
    if t then
        return t[2]
    end

    return 0.5
end

-- 获取模型的光圈半径
function ConstModelConfig.GetCircleRadius(name)
    local t = model_config_table.data[name]
    if t then
        local value = t[6]
        if value == nil or value == 0 then
            value = ConstModelConfig.GetModelRadius(name)
        end
        return value
    end

    return 0.5
end

function ConstModelConfig.GetMiniMapIconName(name)
    local t = model_config_table.data[name]
    if t then
        return t[3]
    end

    return ''
end

-- 获取模型的高度
function ConstModelConfig.GetModelHeight(name)
    local t = model_config_table.data[name]
    if t then
        return t[4]
    end

    return 1.0
end

function ConstModelConfig.GetTiles(name)
	local t = model_config_table.data[name]
	if t == nil then
		return nil
	end
	
	local str = t[7]
	if string.IsNullOrEmpty(str) then
		return nil
	end

	return string.split_ii_array(str, ',')
end


-- 是否显示光影
function ConstModelConfig.IsShowSunShine(name)
    local t = model_config_table.data[name]
    if t then
        return t[5] == 1
    end

    return false
end

